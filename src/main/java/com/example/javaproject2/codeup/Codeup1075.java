package com.example.javaproject2.codeup;

import java.util.Scanner;

public class Codeup1075 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        while(num!=0) {
            num--;
            System.out.println(num);
        }
    }
}
